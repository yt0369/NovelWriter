"""
草稿扩写技能：将简短大纲扩写为完整章节。
"""
from core.skills.registry import Skill


def create_skill() -> Skill:
    return Skill(
        name="draft_expander",
        display_name="草稿扩写",
        description="将简短大纲或草稿扩写为完整章节——添加细节、对话、描写、过渡",
        keywords=["扩写", "展开", "丰富", "详细", "补充", "扩展"],
        tools=["read_file", "write_file", "patch_file", "query_memory"],
        wing="剧情",
        content="""## 草稿扩写模式

你现在进入草稿扩写模式。你的任务是将简短的大纲、草稿或概要扩写为完整的、有血有肉的章节内容。

### 扩写流程

#### 第一步：理解原始素材

1. **识别核心事件**：这段大纲要推进什么剧情？
2. **识别关键角色**：哪些角色在本段出场？
3. **识别情绪走向**：本段的情绪从哪里开始、到哪里结束？
4. **识别缺失元素**：大纲中哪些地方需要补充？

#### 第二步：确定扩写策略

1. **比例控制**：大纲1句话通常扩写为300-500字
2. **节奏规划**：哪些部分详写、哪些略写
3. **场景划分**：一段大纲可能包含多个场景
4. **过渡设计**：场景之间的衔接方式

#### 第三步：分层扩写

##### 层次一：骨架——事件与动作

将大纲中的事件转化为具体的角色动作和事件序列：

大纲：主角进入森林，遇到危险

扩写骨架：
- 主角站在森林入口，犹豫片刻后踏入
- 林中光线昏暗，脚下落叶沙沙作响
- 突然听到异常声响，主角警觉
- 危险出现，主角应对

##### 层次二：血肉——描写与细节

为骨架添加感官描写和环境细节：

- 视觉：光线、色彩、空间感
- 听觉：环境音、关键声响
- 触觉：温度、质感、身体感受
- 嗅觉：环境气味
- 心理：角色的内心活动

##### 层次三：灵魂——对话与情感

添加角色对话和情感层次：

- 对话推动剧情或揭示性格
- 情感反应让角色更真实
- 潜台词增加深度
- 情感变化形成弧线

### 扩写技巧

#### 从大纲到场景

| 大纲类型 | 扩写重点 | 常见问题 |
|----------|----------|----------|
| 动作大纲 | 动作细节、节奏、感官 | 变成流水账 |
| 对话大纲 | 对话内容、潜台词、语气 | 变成说明文 |
| 转折大纲 | 铺垫、心理变化、冲击感 | 转折太突兀 |
| 情感大纲 | 细节、回忆、身体反应 | 直接说"他很伤心" |
| 描写大纲 | 五感、氛围、角色视角 | 堆砌形容词 |

#### 对话扩写

大纲：主角和导师讨论计划

扩写要点：
1. 开场：用一句有性格的台词切入
2. 交锋：双方观点碰撞，不是一问一答
3. 潜台词：说的和想的不一样
4. 推进：对话要推动剧情或揭示信息
5. 收束：用一句有力的话结束

#### 动作扩写

大纲：主角与敌人战斗

扩写要点：
1. 战前：紧张感的营造
2. 开战：第一击的冲击力
3. 过程：攻防转换、节奏变化
4. 转折：局势的变化
5. 结束：决定性的一击或意外

#### 过渡扩写

大纲：主角从A地到B地

扩写要点：
1. 离开：对A地的最后印象
2. 途中：旅途中的思考或小事件
3. 到达：对B地的第一印象
4. 如果不重要：用简短过渡一笔带过

### 扩写原则

1. **忠于大纲**：扩写不是改写，核心事件不可变更
2. **适度原则**：不是所有地方都需要详写，详略得当
3. **角色驱动**：通过角色的选择和反应来展开，而非外部叙述
4. **保持节奏**：扩写后的节奏应与大纲意图一致
5. **前后衔接**：扩写内容必须与上下文自然衔接
6. **避免注水**：每句话都应有存在的价值，不为凑字数而写

### 双轴判断检查清单

- [ ] 驱动力：这段内容是否让读者想继续翻页？属于哪种驱动力？
- [ ] 目的性：删掉这段内容，读者会少获得什么？
- [ ] 一致性：是否与已建立的设定/角色/时间线一致？
- [ ] 展示而非告知：是否通过细节而非直接描述来传达信息？
""",
    )
