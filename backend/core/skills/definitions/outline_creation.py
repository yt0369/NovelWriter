"""
大纲创建技能：从零开始创建小说大纲。
"""
from core.skills.registry import Skill


def create_skill() -> Skill:
    return Skill(
        name="outline_creation",
        display_name="大纲创建",
        description="从零开始创建小说大纲——前提、角色、三幕结构、章节分解",
        keywords=["创建大纲", "新建大纲", "从零", "开始规划", "故事框架"],
        tools=["read_file", "write_file", "query_memory", "manage_memory"],
        wing="剧情",
        content="""## 大纲创建模式

你现在进入大纲创建模式。你的任务是从零开始帮助用户构建一个完整的小说大纲。

### 创建步骤

#### 第一步：确立核心前提

在开始任何规划之前，必须先明确故事的核心：

1. **一句话概括**：用一句话说清楚这个故事讲什么
   - 格式：「一个{什么样的}主角，为了{什么目标}，面对{什么障碍}，最终{什么结果}」
   - 示例：一个失去记忆的剑客，为了找回自己的过去，面对追杀他的神秘组织，最终发现自己的身份比想象中更加可怕

2. **核心冲突**：故事的根本矛盾是什么
3. **主题**：故事想表达什么
4. **情感基调**：整体是热血/黑暗/温馨/悬疑

#### 第二步：设计核心角色

1. **主角**
   - 外在目标：想要达成什么
   - 内在需求：真正需要什么
   - 核心矛盾：目标与需求之间的冲突
   - 致命缺陷：阻碍成长的性格弱点

2. **对手/反派**
   - 与主角的冲突本质
   - 自己的合理动机
   - 与主角的镜像关系

3. **关键配角**
   - 每个配角的功能定位
   - 与主角的关系
   - 自身的小弧线

#### 第三步：构建三幕结构

##### 第一幕：建置（约占25%）

1. **开场画面**：展示主角的日常世界
2. **激励事件**：打破平衡的突发事件
3. **拒绝冒险**：主角的犹豫与抗拒
4. **第一转折**：主角被迫/主动踏上旅程
5. **第一幕高潮**：无法回头的决定性时刻

##### 第二幕：对抗（约占50%）

**上半场：**
1. **进入新世界**：主角适应新的规则
2. **盟友与敌人**：建立关系网络
3. **中点转折**：重大揭示或假胜利
4. **局势升级**：赌注提高

**下半场：**
5. **敌人反击**：反派加强攻势
6. **失去一切**：主角跌入最低谷
7. **灵魂黑夜**：主角的内心崩溃与重建
8. **第二幕高潮**：重新站起来

##### 第三幕：解决（约占25%）

1. **最终准备**：主角以新的姿态面对最终挑战
2. **高潮对决**：与核心冲突的正面交锋
3. **转折揭示**：出乎意料但合情合理的真相
4. **结局**：新的平衡状态
5. **终场画面**：与开场形成对比的收束

#### 第四步：章节分解

将三幕结构细化为具体章节：

```markdown
# {小说名} 大纲

## 核心前提
{一句话概括}

## 主要角色
- {主角}：{简述}
- {反派}：{简述}
- {配角1}：{简述}

## 章节大纲

### 第一幕：{幕名}
#### 第1章：{章名}
- 场景：{地点}
- 事件：{发生什么}
- 推进：{推进了什么剧情线}
- 情绪：{本章情绪基调}
- 结尾钩子：{留什么悬念}

#### 第2章：{章名}
...

### 第二幕：{幕名}
...

### 第三幕：{幕名}
...
```

### 第五步：写后校验

大纲完成后，必须进行以下检查：

1. **一致性检查**：
   - 角色行为是否符合设定？
   - 时间线是否自洽？
   - 世界观规则是否被违反？

2. **伏笔覆盖检查**：
   - 所有伏笔是否都有回收计划？
   - 回收间隔是否合理（3-5 章）？
   - 是否有遗漏的伏笔？

3. **节奏合理性检查**：
   - 是否有连续多章的平淡段落？
   - 高潮分布是否均匀？
   - 每章是否有至少 1 个钩子？

4. **双轴检查**：
   - 每章的驱动力是什么？
   - 删掉哪章读者会少获得什么？

### 引导树

用户可能的需求：
1. **讨论方向**：用户想聊创作方向，不急着写大纲
2. **从零创建**：全新项目，需要完整大纲
3. **补全已有**：已有部分大纲，需要补充
4. **扩展某一卷**：已有整体大纲，需要细化某一卷
5. **修改已创建**：已有大纲，需要调整

### 创建原则

1. **从大到小**：先确定整体结构，再填充细节
2. **留有弹性**：大纲是地图不是牢笼，写作中可以调整
3. **冲突驱动**：每一章都应有冲突或张力
4. **钩子意识**：每章结尾都应让读者想继续
5. **伏笔规划**：在大纲阶段就规划好伏笔和回收
6. **角色优先**：剧情由角色选择驱动，而非角色被剧情推着走
""",
    )
