"""
角色状态追踪技能：追踪角色在章节间的状态变化。
"""
from core.skills.registry import Skill


def create_skill() -> Skill:
    return Skill(
        name="character_status",
        display_name="角色状态追踪",
        description="追踪角色在章节间的状态变化——位置、情绪、能力、关系、物品",
        keywords=["状态", "变化", "成长", "发展", "当前", "位置", "能力"],
        tools=["read_file", "query_memory", "manage_memory"],
        wing="角色",
        content="""## 角色状态追踪模式

你现在进入角色状态追踪模式。你的任务是确保角色在各章节之间的状态保持连贯，不出现矛盾或遗漏。

### 追踪维度

#### 一、物理位置

1. **当前位置**：角色此刻在哪里
2. **移动轨迹**：角色如何从上一位置到达当前位置
3. **同行者**：谁和角色在一起
4. **位置变化原因**：为什么移动到此处

追踪要点：
- 角色不可能同时出现在两个地方
- 移动需要时间，不可瞬移（除非设定允许）
- 分离的角色再次相遇需要合理的契机

#### 二、情绪状态

1. **当前情绪**：角色此刻的主要情绪
2. **情绪来源**：是什么导致了这种情绪
3. **情绪强度**：轻微/中等/强烈/极端
4. **情绪趋势**：上升/平稳/下降/波动

情绪变化规则：
- 情绪变化需要触发事件
- 强烈情绪不会瞬间消失
- 压抑的情绪需要出口
- 多种情绪可以并存

#### 三、能力状态

1. **当前能力值**：力量、速度、法力等
2. **能力变化**：增强/减弱/觉醒/封印
3. **消耗与恢复**：体力和法力的消耗与恢复
4. **新获得能力**：最近学会的技能或力量
5. **能力限制**：当前存在的限制或封印

追踪要点：
- 受伤后战力应下降
- 能力提升需要过程
- 能力使用有代价
- 不可遗忘已获得的能力

#### 四、人际关系

1. **关系变化**：与其他角色的关系如何变化
2. **新关系**：新建立的关系
3. **关系裂痕**：出现矛盾或疏远的关系
4. **信任等级**：对其他角色的信任程度

#### 五、物品与资源

1. **持有物品**：角色当前携带的物品
2. **获得物品**：新获得的物品及来源
3. **失去物品**：消耗、丢失或赠出的物品
4. **关键道具**：对剧情有影响的物品状态

### 状态追踪模板

```markdown
# {角色名} 状态追踪

## 最新状态（第X章）
- 位置：{当前位置}
- 情绪：{当前情绪}（强度：{X}/10）
- 体力：{X}/10
- 同行者：{同行角色}

## 能力状态
- 战斗力：{等级}
- 特殊能力：{列表}
- 当前限制：{限制说明}

## 关键物品
- {物品1}：{状态}
- {物品2}：{状态}

## 重要关系变化
- 与{角色A}：{关系描述}（变化：{增/减/不变}）

## 本章关键变化
- {变化1}
- {变化2}
```

### 追踪原则

1. **每章更新**：每完成一章，更新相关角色的状态
2. **变化必有因**：任何状态变化都必须有明确的原因
3. **不遗忘**：已记录的状态不可无故消失
4. **前后一致**：新状态必须与旧状态逻辑衔接
5. **存入记忆**：将状态变化写入记忆库，供后续章节查询

### 静态蓝图 vs 动态快照

**静态蓝图**（角色档案）：
- 不变的基本设定：姓名、性别、年龄、出身
- 底色：核心性格特质
- 固有能力：天赋、体质
- 背景故事：过去经历

**动态快照**（角色最新情况）：
- 当前位置和状态
- 当前情绪和心理状态
- 当前能力值和限制
- 当前关系和信任度
- 当前持有物品

**查询优先级**：
1. 角色最新情况（动态快照）
2. 章节细纲/正文（上下文）
3. 角色档案（静态蓝图）

**更新时机**：
- 位置变化
- 获得/失去物品
- 受伤/恢复
- 关系变化
- 心理转变
- 知晓新信息

### 常见错误

1. **位置跳跃**：角色上一章在A地，下一章突然在B地，无交代
2. **伤势遗忘**：受重伤后下一章完好如初
3. **能力遗忘**：获得的能力在后续战斗中未使用
4. **物品消失**：关键道具突然不再提及
5. **关系断裂**：建立的友谊或敌意在后续章节中无体现
""",
    )
